const Tour = require('../models/tourModel');
const APIFeatures = require('../utils/apiFeatures');

exports.aliasTop5Tour = (req, res, next) => {
  req.query.limit = 5;
  req.query.sort = 'price,-ratingAverage';
  req.query.fields = 'name,duration,difficulty,price,ratingAverage';
  next();
};

exports.getAllTour = async (req, res) => {
  try {
    //EXECUTE QUERY

    const featuers = new APIFeatures(Tour.find(), req.query)
      .filter()
      .sort()
      .fieldLimit()
      .paginate();

    const tours = await featuers.query;

    //SEND RESPONSE

    res.status(200).json({
      status: 'success',
      results: tours.length,
      data: {
        tours
      }
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      massage: err.massage
    });
  }
};

